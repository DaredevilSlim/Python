c = 0
i = 0
j = 5
while len(s) >= j:
    if '>>-->' in s[i:j]:
        c += 1
        i += 4
        j += 4
    elif '<--<<' in s[i:j]:
        c += 1
        i += 4
        j += 4
    else:
        i += 1
        j += 1
