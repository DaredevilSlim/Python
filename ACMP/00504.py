# ЗАДАЧА №504 - Цветочки
# (Время: 1 сек. Память: 16 Мб Сложность: 12%)
# В рождественский вечер на окошке стояло три цветочка, слева на право: герань, крокус и фиалка. Каждое утро Маша
# вытирала окошко и меняла местами стоящий справа цветок с центральным цветком. А Таня каждый вечер поливала цветочки и
# меняла местами левый и центральный цветок. Требуется определить порядок цветов ночью по прошествии K дней.
# Входные данные - Во входном файле INPUT.TXT содержится натуральное число K – число дней (K ≤ 1000).
# Выходные данные - В выходной файл OUTPUT.TXT требуется вывести три английских буквы: «G», «C» и «V» (заглавные буквы
# без пробелов), описывающие порядок цветов на окошке по истечении K дней (слева направо). Обозначения: G – герань,
# C – крокус, V – фиалка.
d = int(input()) % 3
print('GCV' if d == 0 else 'CVG' if d == 2 else 'VGC')
# Или
d = int(input())
if d % 3 == 0:
    print('GCV')
elif d % 3 == 2:
    print('CVG')
else:
    print('VGC')
