# ЗАДАЧА №529 - Длина отрезка
# (Время: 1 сек. Память: 16 Мб Сложность: 12%)
# Отрезок, заданный координатами своих концевых точек
# Отрезок задан координатами своих концевых точек. Требуется вычислить длину этого отрезка.
# Входные данные - Входной файл INPUT.TXT содержит координаты концов отрезка в формате X1 Y1 X2 Y2 . Все координаты –
# целые числа, не превышающие 1000 по абсолютной величине.
# Выходные данные - В выходной файл OUTPUT.TXT выведите длину отрезка с точностью 10-5.
a, b, c, d = map(int, input().split())
print('{0:.5f}'.format(((a - c) ** 2 + (b - d) ** 2) ** 0.5))
