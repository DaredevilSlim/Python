# 1.1.1. Определение структуры данных
print('1.1.1. Определение структуры данных')
# Структура данных — это специальное решение для организации данных, которое предоставляет место для размещения
# элементов и возможность для их сохранения и извлечения. А именно, хотя бы один метод для добавления нового элемента в
# структуру и один метод либо для извлечения указанного элемента, либо для выполнения запроса к структуре данных.
# Самый простой пример структуры данных — это массив. Так, массив символов обеспечивает хранилище для конечного числа
# символов и методы для извлечения каждого символа в зависимости от его позиции в массиве. На рис. 1.1 показано,
# как хранится array = ['C', 'A', 'R']: массив символов, в котором символы C, A и R размещаются таким образом, чтобы,
# например, элемент array[1] соответствовал значению А.
# Структуры данных могут быть абстрактными или конкретными. Между собой они соотносятся так, как указано в перечне ниже.
# - Абстрактный тип данных (АТД) определяет операции, которые могут выполняться с некоторыми данными, и вычислительную
# сложность этих операций. Никаких подробностей о том, как хранятся данные или как используется физическая память, не
# приводится.
# - Структура данных (СД) — это конкретная реализация спецификации, предоставляемой АТД.
