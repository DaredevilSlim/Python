# Входные данные - Входная строка содержит два вещественных числа, разделённых пробелом.
# Выходные данные - Нужно вывести одно значение – x ** y – с тремя знаками в дробной части.
x, y = map(float, input().split())
print('{0:.3f}'.format(x ** y))


