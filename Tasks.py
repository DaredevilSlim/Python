# Задача №112145. Три числа
# Напишите программу, которая находит сумму, произведение и среднее арифметическое трех целых чисел, введенных с
# клавиатуры.
# Входные данные - Три целых числа вводятся в одной строке через пробелы.
# Выходные данные - Программа должна вывести сумму, произведение и среднее арифметическое введенных чисел. Среднее
# арифметическое нужно вывести с точностью 3 знака после десятичной точки (ровно 3 знака с учетом округления).
a, b, c = map(int, input().split())
sum_abc = a + b + c
mult_abc = a * b * c
aver_abc = (a + b + c) / 3
print('{0:}+{1:}+{2:}={3:}'.format(a, b, c, sum_abc))
print('{0:}*{1:}*{2:}={3:}'.format(a, b, c, mult_abc))
print('({0:}+{1:}+{2:})/3={3:.3f}'.format(a, b, c, aver_abc))


# Задача №112146. Круг
# Напишите программу, которая вводит радиус круга и вычисляет его площадь и длину окружности.
# Входные данные - Входная строка содержит одно число - радиус круга.
# Выходные данные - В первой строке должно быть выведена площадь круга, а во второй строке - длина окружности. Оба
# значения выводятся с тремя знаками в дробной части.
from math import pi

r = int(input())
s = pi * r**2
p = 2 * pi * r
print('{0:.3f}'.format(s))
print('{0:.3f}'.format(p))

# Или
from math import pi

r = int(input())
print('{0:.3f}'.format(pi * r ** 2))
print('{0:.3f}'.format(2 * pi * r))


# Задача №112147. 10-я степень
# Напишите программу, которая возводит введенное число в степень 10, используя только четыре операции умножения.
# Входные данные - Входная строка содержит единственное вещественное число в диапазоне от 0 до 8.
# Выходные данные - Нужно вывести 10-ю степень полученного числа с тремя знаками в дробной части.
a = float(input())
b = a * a
c = b * b
d = c * c
e = d * b
print('{0:.3f}'.format(e))


# Задача №112148. Разбить на цифры
# Напишите программу, которая вводит трехзначное число и разбивает его на цифры. Например, при вводе числа 123 программа
# должна вывести «1 2 3».
# Входные данные - Входная строка содержит одно трехзначное число.
# Выходные данные - Программа должна вывести три цифры переданного ей числа, начиная с первой, разделенные пробелами.
n = int(input())
l = n % 10
m = (n % 100) // 10
f = n // 100
print(f, m, l)

# Или
n = int(input())
print(n // 100, (n % 100) // 10, n % 10)


# Задача №112149. Расстояние
# Напишите программу, которая вводит координаты двух точек на числовой оси и выводит расстояние между ними.
# Входные данные - В первой строке вводятся через пробел координаты первой точки(сначала x - координата, потом
# y - координата), во второй строке в том же порядке – координаты второй точки. Все координаты – вещественные числа.
# Выходные данные - Программа должна вывести одно число: расстояние между точками с тремя знаками в дробной части.
x_one, y_one = map(float, input().split())
x_two, y_two = map(float, input().split())
distance = (abs(x_one - x_two) ** 2 + abs(y_one - y_two) ** 2) ** 0.5
print('{0:.3f}'.format(distance))


# Задача №112150. Степень x ** y
